class NumbersSwapping
{
	public static void main(String[]arg)

	{
		int a = 3;
		int b = 5;
		
		System.out.println("Before Swapping : " + a);
		System.out.println("Before Swapping : " + b);
	
		 a = a + b;
		 b = a - b;
		 a = a - b;
	
	
		System.out.println("after Swapping : " + a);
		System.out.println("after Swapping : " + b);
	
	
	
	
	}

}